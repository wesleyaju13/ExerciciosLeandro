#include <iostream>
#include <string>

using namespace std;

main(){
	int v1, v2;
		
	cout << " Digite o primeiro valor: " << endl;
	cin >> v1;
	cout << " Digite o segundo valor: " << endl;
	cin >> v2;
	
	cout << "O resultado da Soma eh: " << v1+v2 << endl;
	cout << "O resultado da Subtracao eh: " << v1-v2 << endl;
	cout << "O resultado da Multiplicao eh: " << v1*v2 << endl;
	cout << "O resultado da Divisao eh: " << v1/v2 << endl;
	cout << "O resultado do Modulo(resto) eh: " << v1%v2 << endl;
	
return 0;
}
